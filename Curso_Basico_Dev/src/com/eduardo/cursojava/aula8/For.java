package com.eduardo.cursojava.aula8;

public class For {

	public static void main(String[] args) {
		
		for (int i=0;i < 5;i++) {
			System.out.println("i tem valor:" + i);
		}
		
		for (int i= 0, j = 10; i <= j ;i++, j--) {
			System.out.println("i tem valor:" + i  +" "+ "J tem valor:" + j);
			
		}
	}

}
